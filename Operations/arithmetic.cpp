#include <iostream>
using namespace std;

int main()
{
	int a = 8, b= 4;
	
	cout << "A: " << a << " B: " << b << endl;
	cout << "A + B: " << a + b << endl;
	cout << "A - B: " << a - b << endl;
	cout << "A * B: " << a * b << endl;
	cout << "A / B: " << a / b << endl;
	cout << "A % B: " << a % b << endl;
	cout << "A++: " << a++ << endl;
	cout << "A: " << a << endl;
	cout << "++B: " << ++b << endl;
	cout << "B: " << b << endl;
	
	return 0;
}